def test_parse_index_well_formed(tmp_path):
    repo = _write_repo(tmp_path)
    index = parse_index(repo / "plans" / "README.md")
    assert [row.plan for row in index.rows] == ["001", "002", "003"]
    row = index.rows[0]
    assert row.title == "Wire the frobnicator"
    assert row.priority == "P1"
    assert row.effort == "M"
    assert row.status == "done"
    assert index.rows[1].depends_on == "001"


def test_parse_index_blocked_reason_captured(tmp_path):
    repo = _write_repo(tmp_path)
    index = parse_index(repo / "plans" / "README.md")
    blocked = index.rows[2]
    assert blocked.status == "blocked"
    assert blocked.status_reason == "waiting on upstream #42"


def test_parse_index_extra_column_tolerated(tmp_path):
    index_md = """\
## Execution order & status

| Plan | Owner | Title | Priority | Effort | Depends on | Status |
|------|-------|-------|----------|--------|------------|--------|
| 001  | mac   | Wire the frobnicator | P1 | M | — | TODO |
"""
    repo = tmp_path / "repo"
    (repo / "plans").mkdir(parents=True)
    (repo / "plans" / "README.md").write_text(index_md, encoding="utf-8")
    index = parse_index(repo / "plans" / "README.md")
    assert len(index.rows) == 1
    assert index.rows[0].plan == "001"
    assert index.rows[0].title == "Wire the frobnicator"
    assert index.rows[0].status == "todo"


def test_parse_index_rejected_findings_bullets(tmp_path):
    repo = _write_repo(tmp_path)
