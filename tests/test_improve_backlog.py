def test_rollup_counts_and_next_executable(tmp_path):
    repo = _write_repo(tmp_path)
    index = parse_index(repo / "plans" / "README.md")
    metas = {
        "001": parse_plan_status(repo / "plans" / "001-wire-the-frobnicator.md"),
        "002": parse_plan_status(repo / "plans" / "002-ship-the-doodad.md"),
        "003": None,
    }
    entities = build_entities("frob-repo", index, metas)
    rollup = next(e for e in entities if e["name"] == "improve-backlog:frob-repo")
    # 002 is the lowest-numbered TODO whose only dep (001) is DONE.
    assert "Next executable plan: 002" in rollup["summary"]
    assert "1 done" in rollup["summary"]
    assert "1 blocked" in rollup["summary"]


def test_plan_entity_summary_carries_status_and_reason(tmp_path):
    repo = _write_repo(tmp_path)
    index = parse_index(repo / "plans" / "README.md")
    entities = build_entities("frob-repo", index, {"001": None, "002": None, "003": None})
    blocked = next(e for e in entities if e["name"] == "plan:frob-repo/003")
