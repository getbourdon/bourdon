def test_build_entities_missing_plan_file_no_crash(tmp_path):
    repo = _write_repo(tmp_path)
    library = _library(tmp_path)
    result = sync(repo, library)
    # Plan 003 has no plan file: entity name falls back to the plan number.
    store = L6Store(library)
    manifest = store.get_agent_manifest("improve", access_level="public")
    names = {e["name"] for e in manifest["known_entities"]}
    assert "plan:frob-repo/003" in names
