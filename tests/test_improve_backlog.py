def test_entity_names_stable_across_two_syncs(tmp_path):
    repo = _write_repo(tmp_path)
    library = _library(tmp_path)
    first = sync(repo, library)
    assert first["entities_added"] == 4
    second = sync(repo, library)
    # Merge-dedupe by name: second sync updates, never re-adds.
    assert second["entities_added"] == 0
    assert second["entities_updated"] == 4
    assert second["total_entities"] == 4


def test_dry_run_performs_no_write(tmp_path, capsys):
    repo = _write_repo(tmp_path)
    library = _library(tmp_path)
    payload = sync(repo, library, dry_run=True)
    assert payload["dry_run"] is True
    out = capsys.readouterr().out
    assert "plan:frob-repo/001-wire-the-frobnicator" in out
    assert list((library / "agents").iterdir()) == []  # library dir untouched


def test_dry_run_survives_narrow_codepage_stdout(tmp_path, monkeypatch, capsys):
    """cp1252 consoles can't encode e.g. U+2192; dry-run must degrade, not crash."""
    import builtins

    repo = _write_repo(tmp_path)
    plans = repo / "plans"
    index = (plans / "README.md").read_text(encoding="utf-8")
    (plans / "README.md").write_text(
        index.replace("Wire the frobnicator", "Wire → frobnicator"), encoding="utf-8"
    )
    library = _library(tmp_path)

    real_print = builtins.print
    calls = {"n": 0}

    def cp1252_print(*args, **kwargs):
        calls["n"] += 1
        if calls["n"] == 1:
            # Simulate a console that cannot encode the arrow.
            str(args[0]).encode("cp1252")
        real_print(*args, **kwargs)

    monkeypatch.setattr(builtins, "print", cp1252_print)
    payload = sync(repo, library, dry_run=True)
    assert payload["dry_run"] is True
    out = capsys.readouterr().out
    assert "frobnicator" in out  # fallback actually printed
    assert list((library / "agents").iterdir()) == []


def test_sync_round_trips_through_l6store(tmp_path):
    repo = _write_repo(tmp_path)
    library = _library(tmp_path)
    summary = sync(repo, library)
    assert summary["agent_id"] == "improve"
    assert summary["sessions_added"] == 1

    store = L6Store(library)
    manifest = store.get_agent_manifest("improve", access_level="public")
    assert manifest["agent"]["type"] == "other"
    names = {e["name"] for e in manifest["known_entities"]}
    assert "plan:frob-repo/001-wire-the-frobnicator" in names
    assert "improve-backlog:frob-repo" in names
    session = manifest["recent_sessions"][0]
    assert session["project_focus"] == ["frob-repo", "improve-backlog"]
    assert session["key_actions"][0].startswith("synced 3 plans:")
