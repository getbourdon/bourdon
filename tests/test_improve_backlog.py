def test_next_executable_skipped_when_deps_not_done():
    from core.improve_backlog import BacklogRow, _next_executable

    rows = [
        BacklogRow(plan="001", title="a", status="todo", depends_on="—"),
        BacklogRow(plan="002", title="b", status="todo", depends_on="001"),
    ]
    nxt = _next_executable(rows)
    assert nxt is not None and nxt.plan == "001"
    rows[0].status = "in progress"
    # 002's dep is not DONE and 001 is no longer TODO -> nothing executable.
    assert _next_executable(rows) is None


